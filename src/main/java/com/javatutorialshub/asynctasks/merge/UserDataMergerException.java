package com.javatutorialshub.asynctasks.merge;

public class UserDataMergerException extends Throwable {
    public UserDataMergerException() {
    }

    public UserDataMergerException(String message) {
        super(message);
    }

    public UserDataMergerException(String message, Throwable cause) {
        super(message, cause);
    }

    public UserDataMergerException(Throwable cause) {
        super(cause);
    }

    public UserDataMergerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
