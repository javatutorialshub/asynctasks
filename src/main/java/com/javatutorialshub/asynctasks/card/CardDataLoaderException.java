package com.javatutorialshub.asynctasks.card;

public class CardDataLoaderException extends Throwable {
    public CardDataLoaderException() {
    }

    public CardDataLoaderException(String message) {
        super(message);
    }

    public CardDataLoaderException(String message, Throwable cause) {
        super(message, cause);
    }

    public CardDataLoaderException(Throwable cause) {
        super(cause);
    }

    public CardDataLoaderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
